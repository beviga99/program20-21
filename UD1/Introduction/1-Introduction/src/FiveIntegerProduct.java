public class FiveIntegerProduct {   // Save as "FiveIntegerSum.java"
   public static void main(String[] args) {
      int product1 = 11;  // Declare 5 integer variables and assign a value
      int product2 = 22;
      int product3 = 33;
      int product4 = 44;
      int product5 = 55;
      int result;  // Declare an integer variable called sum to hold the sum
      result = product1 * product2 * product3 + product4 + product5;  // Compute sum
      System.out.print("The result is ");  // Print a descriptive string
                                        // Cursor stays after the printed string
      System.out.println(result);  // Print the value stored in variable sum
                                // Cursor advances to the beginning of next line
   }
}
